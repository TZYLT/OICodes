#define M 5005000
int a[M],sum[M],invsum[M],n,nk,k,p;
int ans;
long long qpow(long long a,long long k,long long p){
    long long res=1;
    while(k){
        if(k&1) res=(res*a)%p;
        a=(a*a)%p;
        k>>=1;
    }
    return res%p;
}
