void linearSieve(int n){
    v[1]=1;
    for(int i=2;i<=n;i++){
        if(!v[i])p[++k]=i,mn[i]=i;
        for(int j=1;j<=k&&i*p[j]<=n;j++){
            v[i*p[j]]=1;mn[i*p[j]]=p[j];
            if(i%p[j]==0)break;
        }
    }
}
long long f(int n){
    int t=n;
    long long ans=0,T=1;
    multiset<int> s;
    while(t>1){s.insert(mn[t]);t/=mn[t];}
    vector<pair<int,int> > fac;
    while(s.size()){
        fac.push_back(make_pair(*s.begin(),s.count(*s.begin())));
        int x=*s.begin();
        s.erase(x);
    }
    for(auto c:fac)
        T*=c.second+1;
    for(auto c:fac)
        ans+=c.first*T*c.second;
    return ans/2;
}
